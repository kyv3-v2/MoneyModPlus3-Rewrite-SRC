    public static void drawPolygonOutline(final double x, final double y, final double lineWidth, final int radius, final int sides, final int color) {
        final float alpha = (color >> 24 & 0xFF) / 255.0f;
        final float red = (color >> 16 & 0xFF) / 255.0f;
        final float green = (color >> 8 & 0xFF) / 255.0f;
        final float blue = (color & 0xFF) / 255.0f;
        pushMatrix();
        disableTexture2D();
        enableBlend();
        disableAlpha();
        tryBlendFuncSeparate(770, 771, 1, 0);
        shadeModel(7425);
        glLineWidth((float)lineWidth);
        final Tessellator tessellator = Tessellator.getInstance();
        final BufferBuilder bufferbuilder = tessellator.getBuffer();
        bufferbuilder.begin(2, DefaultVertexFormats.POSITION_COLOR);
        final double TWICE_PI = 6.283185307179586;
        for (int i = 0; i <= sides; ++i) {
            final double angle = 6.283185307179586 * i / sides + Math.toRadians(180.0);
            bufferbuilder.pos(x + Math.sin(angle) * radius, y + Math.cos(angle) * radius, 0.0).color(red, green, blue, alpha).endVertex();
        }
        tessellator.draw();
        shadeModel(7424);
        GL11.glDisable(2848);
        disableBlend();
        enableAlpha();
        enableTexture2D();
        popMatrix();
    }
    

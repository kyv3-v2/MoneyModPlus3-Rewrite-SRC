    public static void drawRect(final float left, final float top, final float right, final float bottom, final int color) {
        final float red = (color >> 16 & 0xFF) / 255.0f;
        final float green = (color >> 8 & 0xFF) / 255.0f;
        final float blue = (color & 0xFF) / 255.0f;
        final float alpha = (color >> 24 & 0xFF) / 255.0f;
        disableTexture2D();
        enableBlend();
        disableAlpha();
        tryBlendFuncSeparate(GlStateManager.SourceFactor.SRC_ALPHA, GlStateManager.DestFactor.ONE_MINUS_SRC_ALPHA, GlStateManager.SourceFactor.ONE, GlStateManager.DestFactor.ZERO);
        shadeModel(7425);
        GL11.glEnable(2848);
        GL11.glHint(3154, 4354);
        final Tessellator tessellator = Tessellator.getInstance();
        final BufferBuilder bufferbuilder = tessellator.getBuffer();
        bufferbuilder.begin(7, DefaultVertexFormats.POSITION_COLOR);
        bufferbuilder.pos((double)left, (double)top, 0.0).color(red, green, blue, alpha).endVertex();
        bufferbuilder.pos((double)left, (double)bottom, 0.0).color(red, green, blue, alpha).endVertex();
        bufferbuilder.pos((double)right, (double)bottom, 0.0).color(red, green, blue, alpha).endVertex();
        bufferbuilder.pos((double)right, (double)top, 0.0).color(red, green, blue, alpha).endVertex();
        tessellator.draw();
        GL11.glDisable(2848);
        shadeModel(7424);
        disableBlend();
        enableAlpha();
        enableTexture2D();
    }
    
    public static void drawRoundedRect(final float left, final float top, final float right, final float bottom, final int smooth, final Color color) {
        drawRect(left + smooth, top, right - smooth, bottom, color.getRGB());
        drawRect(left, top + smooth, right, bottom - smooth, color.getRGB());
        drawPolygon((int)left + smooth, (int)top + smooth, smooth, 360, color.getRGB());
        drawPolygon((int)right - smooth, (int)top + smooth, smooth, 360, color.getRGB());
        drawPolygon((int)right - smooth, (int)bottom - smooth, smooth, 360, color.getRGB());
        drawPolygon((int)left + smooth, (int)bottom - smooth, smooth, 360, color.getRGB());
    }
    
